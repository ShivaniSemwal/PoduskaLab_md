<TD width="*"  align="left" valign="center" >  The self-assembly of colloidal microspheres has been 
explored extensively to study the fundamental properties of crystallization and as a means to create photonic band gap crystals, optical sensors and antireflection coatings. Their periodic lattices can also serve as a template to make magnetic patterns, with potential applications as magnetic memory storage materials. <a href="?content=pubs&topic=research">We employ electrochemical methods to produce magnetic materials, and we investigate how templates can be used to confine their growth and affect their local and long-range magnetic properties.</a> <br><br> Collaborators: <a href="http://www.physics.mun.ca/~anand">Dr. Anand Yethiraj</a> and <a href="http://www.physics.mun.ca/~plumer">Dr. Martin Plumer</a>, Memorial Physics 
