<br>

For as long as humans have existed, we have used materials around us in creative ways to enhance our lives. In ancient times, these materials included ceramics, lime plasters, pigments, and charcoals. In modern life, we use high-tech materials in everything from medical implants (biomaterials) to data storage and retrieval (magnetic materials) to sensors (electronic materials). Then and now, many factors affect the performance of a material, including its
composition and the amount of structural order that is introduced during its preparation and use. Therefore, experiments that investigate how structural order affects the magnetic, optical, electronic, or mechanical properties of solid materials are very important from scientific, technological, and cultural standpoints.</p>

