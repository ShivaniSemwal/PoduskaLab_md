I use physics to make complex problems more tractable.*  I enjoy working with <a href="?content=research&topic=research">interdisciplinary teams</a> in order to learn which problems are the most useful to solve.  I am particularly interested in research problems related to improving environmental sustainability, including their science and social justice aspects.

<p>

Those who work in my research group gain valuable generalist training in materials physics that can be applied to <a href="?content=projects&topic=research">unique fundamental and applied research challenges</a>. Much of our recent work relates to solid forms of carbon-rich materials, including carbonate minerals.

<p>

